public class ViewReturPembelian {
    private String no;
    private String no_retur;
    private String faktur;
    private String tanggal;
    private String kode_supplier;
    private String nama_supplier;
    private String total_nilai_retur;
    private String total_dibayar;
    private String total_mengurangi_hutang;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getNo_retur() {
        return no_retur;
    }

    public void setNo_retur(String no_retur) {
        this.no_retur = no_retur;
    }

    public String getFaktur() {
        return faktur;
    }

    public void setFaktur(String faktur) {
        this.faktur = faktur;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getKode_supplier() {
        return kode_supplier;
    }

    public void setKode_supplier(String kode_supplier) {
        this.kode_supplier = kode_supplier;
    }

    public String getNama_supplier() {
        return nama_supplier;
    }

    public void setNama_supplier(String nama_supplier) {
        this.nama_supplier = nama_supplier;
    }

    public String getTotal_nilai_retur() {
        return total_nilai_retur;
    }

    public void setTotal_nilai_retur(String total_nilai_retur) {
        this.total_nilai_retur = total_nilai_retur;
    }

    public String getTotal_dibayar() {
        return total_dibayar;
    }

    public void setTotal_dibayar(String total_dibayar) {
        this.total_dibayar = total_dibayar;
    }

    public String getTotal_mengurangi_hutang() {
        return total_mengurangi_hutang;
    }

    public void setTotal_mengurangi_hutang(String total_mengurangi_hutang) {
        this.total_mengurangi_hutang = total_mengurangi_hutang;
    }
    
    
}
