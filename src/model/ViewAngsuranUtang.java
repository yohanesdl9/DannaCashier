public class ViewAngsuranUtang {
    private String no;
    private String faktur;
    private String tanggal;
    private String faktur_utang;
    private String kode_supplier;
    private String nama_supplier;
    private String tunai;
    private String operator;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getFaktur() {
        return faktur;
    }

    public void setFaktur(String faktur) {
        this.faktur = faktur;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getFaktur_utang() {
        return faktur_utang;
    }

    public void setFaktur_utang(String faktur_utang) {
        this.faktur_utang = faktur_utang;
    }

    public String getKode_supplier() {
        return kode_supplier;
    }

    public void setKode_supplier(String kode_supplier) {
        this.kode_supplier = kode_supplier;
    }

    public String getNama_supplier() {
        return nama_supplier;
    }

    public void setNama_supplier(String nama_supplier) {
        this.nama_supplier = nama_supplier;
    }

    public String getTunai() {
        return tunai;
    }

    public void setTunai(String tunai) {
        this.tunai = tunai;
    }

    public String getOperator() {
        return operator;
    }

    public void setOperator(String operator) {
        this.operator = operator;
    }
    
    
}
