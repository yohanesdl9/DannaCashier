public class ViewAngsuranPiutang {
    private String no;
    private String faktur;
    private String tanggal;
    private String faktur_piutang;
    private String kode_pelanggan;
    private String nama_pelanggan;
    private String tunai;
    private String operator;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getFaktur() {
        return faktur;
    }

    public void setFaktur(String faktur) {
        this.faktur = faktur;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getFaktur_piutang() {
        return faktur_piutang;
    }

    public void setFaktur_piutang(String faktur_piutang) {
        this.faktur_piutang = faktur_piutang;
    }

    public String getKode_pelanggan() {
        return kode_pelanggan;
    }

    public void setKode_pelanggan(String kode_pelanggan) {
        this.kode_pelanggan = kode_pelanggan;
    }

    public String getNama_pelanggan() {
        return nama_pelanggan;
    }

    public void setNama_pelanggan(String nama_pelanggan) {
        this.nama_pelanggan = nama_pelanggan;
    }

    public String getTunai() {
        return tunai;
    }

    public void setTunai(String tunai) {
        this.tunai = tunai;
    }

    public String getOperator() {
        return operator;
    }

    public void setOperator(String operator) {
        this.operator = operator;
    }
    
    
}
